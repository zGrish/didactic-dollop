# accessing items from a list
programmers = [ 'ada lovelace', 'bill gates', 'steve jobs', 'linus torvalds', 'mark zuckerberg' ]
print(f'The first name in the list is: {programmers[0].title()}')
print(f'The third name in the list is: {programmers[2].title()}')
print(f'The name before the last name in the list is: {programmers[-2].title()}')
print(f'The last name in the list is: {programmers[-1].title()}')
       